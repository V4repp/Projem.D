package test.day15;

import java.util.Scanner;

public class while_Loop {
    public static void main(String[] args) {

        if (true) {
            System.out.println("Hello World");
        }

        System.out.println("-------------------------------------------------");

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }

        System.out.println("--------------------------------------------------");

        while (true) {

            System.out.println("Hello World");
        }
    }
}
